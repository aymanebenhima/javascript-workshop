// concat(): it takes many substrings and joins them.
// Syntax: string.concat(substring, substring, substring)

let string = 'JavaScript '
console.log(string.concat("Is ", "My ", "Favourite ", "Programming ", "Language")) // JavaScriptIsMyFavouriteProgrammingLanguage

let country = 'Mor'
console.log(country.concat("occo")) // Morocco