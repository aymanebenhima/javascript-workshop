// replace(): takes as a parameter the old substring and a new substring.
// syntax: string.replace(oldsubstring, newsubstring)

let string = 'JavaScript Is My Favourite Programming Language'
console.log(string.replace('JavaScript', 'Python')) // Python Is My Favourite Programming Language

let country = 'Morocco'
console.log(country.replace('Mor', 'Noman'))       // Nomanocco